# Bucles For

# Range es una funcion que nos devuelve una secuencia numérica
# range(inicio=0, fin, avance=1)
# range(0,5,1) -> 0, 1, 2, 3, 4
# range(0,6,1) -> 0, 1, 2, 3, 4, 5
# range(7) == range(0,7,1) -> 0, 1, 2, 3, 4, 5, 6
# range(1, 6, 2) -> 1, 3, 5
# range(1, 7, 2) -> 1, 3, 5
# range(5, -1, -1) -> 5, 4, 3, 2, 1, 0
for i in range(5):
    # iteracion1 i = 0
    # iteracion2 i = 1
    # iteracion3 i = 2
    # iteracion4 i = 3
    # iteracion5 i = 4
    print(i)